#include "libft.h"
#include <stdlib.h>

t_list	*ft_lstnew(void *content)
{
	t_list	*element;

	element = (t_list *) malloc(sizeof(*element));
	if (!element)
		return (0);
	element -> content = content;
	element -> next = 0;
	return (element);
}
