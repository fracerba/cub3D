#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*s2;

	s2 = (char *)s;
	c = (char)c;
	i = 0;
	while (*(s2 + i) != '\0')
	{
		if (*(s2 + i) == c)
			return (s2 + i);
		i++;
	}
	if (c == '\0')
		return (s2 + i);
	return (0);
}
